void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	if(GPIO_Pin == FORCE_Button_Pin)
	{
		if(HAL_GPIO_ReadPin(FORCE_Button_GPIO_Port,FORCE_Button_Pin))
		{
			NMOS_SWITCH = 1;
			setGate(NMOS_SWITCH);
			HAL_GPIO_WritePin(FORCE_LED_GPIO_Port,FORCE_LED_Pin,GPIO_PIN_SET);

		}
		else
		{
			NMOS_SWITCH = 0;
			setGate(NMOS_SWITCH);
			HAL_GPIO_WritePin(FORCE_LED_GPIO_Port,FORCE_LED_Pin,GPIO_PIN_RESET);
		}
	}
	else if(GPIO_Pin == LED_BUTTON_Pin)
	{
		if(HAL_GPIO_ReadPin(LED_BUTTON_GPIO_Port,LED_BUTTON_Pin))
		{
			LED_Feedback(BATTERY_STATE);
		}
		else
		{
			LED_Feedback(0);
		}
	}
}

